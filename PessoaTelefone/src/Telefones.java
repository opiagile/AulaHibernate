
public class Telefones {
   private String numero;
   private String tipo;
   
   public Telefones (){}
   
   public void setNumero(String pNumero){
	   this.numero = pNumero;
   }
   public String getNumero(){
	   return this.numero;
   }
   public void setTipo(String pTipo){
	   this.tipo = pTipo;
   }
   public String getTipo(){
	   return this.tipo;
   }

}
